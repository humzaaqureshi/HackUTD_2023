class company:
    def __init__(self, name, country, politics = None, violence = None):
        self.name = name
        self.country = country
        self.politics = politics
        self.violence = violence
    def name(self):
        return self.name
    def country(self):
        return self.country
    def politics(self):
        return self.politics
    def violence(self):
        return self.violence
    

